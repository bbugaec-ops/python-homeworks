# ========== –ó–∞–≤–¥–∞–Ω–Ω—è 5: HybridInstrument –∑ –º–Ω–æ–∂–∏–Ω–Ω–∏–º —É—Å–ø–∞–¥–∫—É–≤–∞–Ω–Ω—è–º ==========

class HybridInstrument(StringInstrument, WindInstrument, PercussionInstrument):
    """
    –ì—ñ–±—Ä–∏–¥–Ω–∏–π —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç, —è–∫–∏–π —É—Å–ø–∞–¥–∫–æ–≤—É—î –≤—ñ–¥ –∫—ñ–ª—å–∫–æ—Ö –∫–ª–∞—Å—ñ–≤
    —Ç–∞ –∫–æ–º–±—ñ–Ω—É—î —ó—Ö –≤–ª–∞—Å—Ç–∏–≤–æ—Å—Ç—ñ —Ç–∞ –º–µ—Ç–æ–¥–∏ —á–µ—Ä–µ–∑ –º–Ω–æ–∂–∏–Ω–Ω–µ —É—Å–ø–∞–¥–∫—É–≤–∞–Ω–Ω—è.
    
    –¶–µ–π –∫–ª–∞—Å –¥–µ–º–æ–Ω—Å—Ç—Ä—É—î –º–Ω–æ–∂–∏–Ω–Ω–µ —É—Å–ø–∞–¥–∫—É–≤–∞–Ω–Ω—è –≤ Python, –∫–æ–º–±—ñ–Ω—É—é—á–∏
    —Ñ—É–Ω–∫—Ü—ñ–æ–Ω–∞–ª—å–Ω—ñ—Å—Ç—å —Å—Ç—Ä—É–Ω–Ω–∏—Ö, –¥—É—Ö–æ–≤–∏—Ö —Ç–∞ —É–¥–∞—Ä–Ω–∏—Ö —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—ñ–≤.
    """
    
    def __init__(self, name="–ì—ñ–±—Ä–∏–¥–Ω–∏–π —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç", volume=5):
        """
        –Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—è –≥—ñ–±—Ä–∏–¥–Ω–æ–≥–æ —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—É
        
        Args:
            name (str): –ù–∞–∑–≤–∞ —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—É
            volume (int): –ì—É—á–Ω—ñ—Å—Ç—å –≤—ñ–¥ 1 –¥–æ 10
        """
        self.name = name
        self.volume = max(1, min(10, volume))  # –û–±–º–µ–∂—É—î–º–æ –≤—ñ–¥ 1 –¥–æ 10
        self.sounds = []  # –ó–±–µ—Ä—ñ–≥–∞—î–º–æ –∑–≤—É–∫–∏ –≤—ñ–¥ —Ä—ñ–∑–Ω–∏—Ö —Ç–∏–ø—ñ–≤ —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—ñ–≤
        self.play_count = 0  # –õ—ñ—á–∏–ª—å–Ω–∏–∫ –≤–∏–∫–ª–∏–∫—ñ–≤ play()
    
    def play(self):
        """
        –ö–æ–º–±—ñ–Ω—É—î –∑–≤—É–∫–∏ –≤—ñ–¥ —É—Å—ñ—Ö —É—Å–ø–∞–¥–∫–æ–≤–∞–Ω–∏—Ö –∫–ª–∞—Å—ñ–≤ —É–Ω—ñ–∫–∞–ª—å–Ω–∏–º —Å–ø–æ—Å–æ–±–æ–º.
        –¶–µ–π –º–µ—Ç–æ–¥ –¥–µ–º–æ–Ω—Å—Ç—Ä—É—î, —è–∫ –º–Ω–æ–∂–∏–Ω–Ω–µ —É—Å–ø–∞–¥–∫—É–≤–∞–Ω–Ω—è –¥–æ–∑–≤–æ–ª—è—î
        –∫–æ–º–±—ñ–Ω—É–≤–∞—Ç–∏ –≤–ª–∞—Å—Ç–∏–≤–æ—Å—Ç—ñ —Ç–∞ –º–µ—Ç–æ–¥–∏ –∑ –∫—ñ–ª—å–∫–æ—Ö –±–∞—Ç—å–∫—ñ–≤—Å—å–∫–∏—Ö –∫–ª–∞—Å—ñ–≤.
        """
        self.play_count += 1
        
        print(f"\nüéµ {self.name} –≥—Ä–∞—î –∫–æ–º–±—ñ–Ω–æ–≤–∞–Ω—É –º–µ–ª–æ–¥—ñ—é (–≥—Ä–∞ #{self.play_count}):")
        print(f"   –ì—É—á–Ω—ñ—Å—Ç—å: {'üîä' * self.volume}{'üîá' * (10 - self.volume)}")
        
        # –û—Ç—Ä–∏–º—É—î–º–æ –∑–≤—É–∫–∏ –≤—ñ–¥ –∫–æ–∂–Ω–æ–≥–æ —Ç–∏–ø—É —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—ñ–≤
        string_sound = "–°—Ç—Ä—É–Ω–Ω–∏–π —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç –≥—Ä–∞—î"
        wind_sound = "–¥—É—Ö–æ–≤–∏–π —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç –≥—Ä–∞—î"
        percussion_sound = "—É–¥–∞—Ä–Ω—ñ —Ç–æ–∂–µ –≥—Ä–∞—é—Ç—å"
        
        # –ö–æ–º–±—ñ–Ω—É—î–º–æ –∑–≤—É–∫–∏ –≤—ñ–¥ —É—Å—ñ—Ö –±–∞—Ç—å–∫—ñ–≤—Å—å–∫–∏—Ö –∫–ª–∞—Å—ñ–≤
        print(f"  üé∏ {string_sound}")
        print(f"  üé∫ {wind_sound}")
        print(f"  ü•Å {percussion_sound}")
        print(f"  ‚Üí –°—Ç–≤–æ—Ä—é—î —É–Ω—ñ–∫–∞–ª—å–Ω–∏–π –æ—Ä–∫–µ—Å—Ç—Ä–æ–≤–∏–π –∑–≤—É–∫! üé∂")
        
        # –ó–±–µ—Ä—ñ–≥–∞—î–º–æ –∑–≤—É–∫–∏ –¥–ª—è –ø–æ–¥–∞–ª—å—à–æ–≥–æ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è
        self.sounds = [string_sound, wind_sound, percussion_sound]
    
    def play_string_part(self):
        """
        –ì—Ä–∞—î —Ç—ñ–ª—å–∫–∏ —Å—Ç—Ä—É–Ω–Ω—É —á–∞—Å—Ç–∏–Ω—É.
        –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î –º–µ—Ç–æ–¥ –∑ –∫–ª–∞—Å—É StringInstrument.
        """
        print("üé∏ –°—Ç—Ä—É–Ω–Ω–∞ —á–∞—Å—Ç–∏–Ω–∞: ", end="")
        StringInstrument.play(self)
    
    def play_wind_part(self):
        """
        –ì—Ä–∞—î —Ç—ñ–ª—å–∫–∏ –¥—É—Ö–æ–≤—É —á–∞—Å—Ç–∏–Ω—É.
        –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î –º–µ—Ç–æ–¥ –∑ –∫–ª–∞—Å—É WindInstrument.
        """
        print("üé∫ –î—É–≥–æ–≤–∞ —á–∞—Å—Ç–∏–Ω–∞: ", end="")
        WindInstrument.play(self)
    
    def play_percussion_part(self):
        """
        –ì—Ä–∞—î —Ç—ñ–ª—å–∫–∏ —É–¥–∞—Ä–Ω—É —á–∞—Å—Ç–∏–Ω—É.
        –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î –º–µ—Ç–æ–¥ –∑ –∫–ª–∞—Å—É PercussionInstrument.
        """
        print("ü•Å –£–¥–∞—Ä–Ω–∞ —á–∞—Å—Ç–∏–Ω–∞: ", end="")
        PercussionInstrument.play(self)
    
    def play_sequence(self, parts=None):
        """
        –ì—Ä–∞—î –ø–æ—Å–ª—ñ–¥–æ–≤–Ω—ñ—Å—Ç—å —á–∞—Å—Ç–∏–Ω —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—É.
        
        Args:
            parts (list): –°–ø–∏—Å–æ–∫ —á–∞—Å—Ç–∏–Ω –¥–ª—è –≤—ñ–¥—Ç–≤–æ—Ä–µ–Ω–Ω—è.
                         –ú–æ–∂–ª–∏–≤—ñ –∑–Ω–∞—á–µ–Ω–Ω—è: 'string', 'wind', 'percussion'
                         –Ø–∫—â–æ None, –≥—Ä–∞—î –≤—Å—ñ —á–∞—Å—Ç–∏–Ω–∏ –ø–æ —á–µ—Ä–∑—ñ.
        """
        if parts is None:
            parts = ['string', 'wind', 'percussion']
        
        print(f"\nüìª –ü–æ—Å–ª—ñ–¥–æ–≤–Ω–µ –≤—ñ–¥—Ç–≤–æ—Ä–µ–Ω–Ω—è —á–∞—Å—Ç–∏–Ω {self.name}:")
        for part in parts:
            if part == 'string':
                self.play_string_part()
            elif part == 'wind':
                self.play_wind_part()
            elif part == 'percussion':
                self.play_percussion_part()
    
    def set_volume(self, volume):
        """
        –í—Å—Ç–∞–Ω–æ–≤–ª—é—î –≥—É—á–Ω—ñ—Å—Ç—å —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—É.
        
        Args:
            volume (int): –ì—É—á–Ω—ñ—Å—Ç—å –≤—ñ–¥ 1 –¥–æ 10
        """
        self.volume = max(1, min(10, volume))
        print(f"üîä –ì—É—á–Ω—ñ—Å—Ç—å {self.name} –≤—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ –Ω–∞ {self.volume}/10")
    
    def get_instrument_types(self):
        """
        –ü–æ–≤–µ—Ä—Ç–∞—î —Å–ø–∏—Å–æ–∫ —Ç–∏–ø—ñ–≤ —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—ñ–≤, –≤—ñ–¥ —è–∫–∏—Ö —É—Å–ø–∞–¥–∫–æ–≤—É—î—Ç—å—Å—è.
        
        Returns:
            list: –°–ø–∏—Å–æ–∫ –Ω–∞–∑–≤ –±–∞—Ç—å–∫—ñ–≤—Å—å–∫–∏—Ö –∫–ª–∞—Å—ñ–≤
        """
        return ["StringInstrument", "WindInstrument", "PercussionInstrument"]
    
    def get_mro(self):
        """
        –ü–æ–≤–µ—Ä—Ç–∞—î –ø–æ—Ä—è–¥–æ–∫ –ø–æ—à—É–∫—É –º–µ—Ç–æ–¥—ñ–≤ (Method Resolution Order).
        
        Returns:
            tuple: MRO –¥–ª—è —Ü—å–æ–≥–æ –∫–ª–∞—Å—É
        """
        return HybridInstrument.__mro__
    
    def show_inheritance_info(self):
        """
        –í–∏–≤–æ–¥–∏—Ç—å –¥–µ—Ç–∞–ª—å–Ω—É —ñ–Ω—Ñ–æ—Ä–º–∞—Ü—ñ—é –ø—Ä–æ –º–Ω–æ–∂–∏–Ω–Ω–µ —É—Å–ø–∞–¥–∫—É–≤–∞–Ω–Ω—è.
        """
        print(f"\nüìö –Ü–Ω—Ñ–æ—Ä–º–∞—Ü—ñ—è –ø—Ä–æ –º–Ω–æ–∂–∏–Ω–Ω–µ —É—Å–ø–∞–¥–∫—É–≤–∞–Ω–Ω—è {self.name}:")
        print(f"   –ù–∞–∑–≤–∞: {self.name}")
        print(f"   –¢–∏–ø–∏ —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—ñ–≤: {', '.join(self.get_instrument_types())}")
        print(f"   –ö—ñ–ª—å–∫—ñ—Å—Ç—å –≤–∏–∫–ª–∏–∫—ñ–≤ play(): {self.play_count}")
